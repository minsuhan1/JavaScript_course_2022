const header = document.querySelector('.header'); // header 섹션
const navHeight = nav.getBoundingClientRect().height; // navigation bar 높이

const stickyNav = function (entries) {
  const [entry] = entries;

  // 뷰포트에서 header 섹션이 사라지는(isIntersecting: false) 시점에 네비게이션 바 요소에 sticky 클래스를 추가
  if (!entry.isIntersecting) nav.classList.add('sticky');
  // 다시 header 섹션이 보이는(isIntersecting: true) 시점에는 고정을 해제한다.
  else nav.classList.remove('sticky');
};

const headerObserver = new IntersectionObserver(stickyNav, {
  root: null, // entire viewport
  threshold: 0, // root의 0% 만큼 header가 보이거나(isIntersecting: true) 사라지면(isIntersecting: false) callback function을 실행
  rootMargin: `-${navHeight}px`, // 뷰포트에서 네비게이션 바 높이만큼 보이거나 남긴 시점을 콜백 메서드 호출시점으로 설정함
});
headerObserver.observe(header);

// Reveal sections
const allSections = document.querySelectorAll('.section');
const revealSection = function (entries, observer) {
  const [entry] = entries;

  if (!entry.isIntersecting) return;

  entry.target.classList.remove('section--hidden');
  observer.unobserve(entry.target);
};

const sectionObserver = new IntersectionObserver(revealSection, {
  root: null,
  threshold: 0.15,
});

allSections.forEach(function (section) {
  sectionObserver.observe(section);
  // section.classList.add('section--hidden');
});

// Lazy loading images
// 1. data-src 속성을 갖는 이미지 요소들(imgTargets)을 선택한 다음, 각각을 observe하도록 만든다.
// 2. imgObserver의 콜백 함수 loadImg는 이미지 요소가 뷰포트와 rootMargin 만큼 교차한 시점에 타겟 요소의 src 값을 data-src 값으로 대체한다.
// 3. 원본 이미지를 로드하고 lazy-img 클래스를 요소에서 제거하여 블러 효과를 없앤다. 이때 원본 이미지가 로드되기 전에 블러 효과가 제거되면 보기 좋지 않으므로 load 이벤트 리스너를 추가하여 원본 이미지 로드가 완료되었을 때 블러 효과를 제거하도록 하였다.
// 4. 원본 이미지가 로드된 경우 해당 요소는 더 이상 감지하지 않는다 (unobserve)

const imgTargets = document.querySelectorAll('img[data-src]');

const loadImg = function (entries, observer) {
  const [entry] = entries;

  if (!entry.isIntersecting) return;

  // Replace src with data-src
  entry.target.src = entry.target.dataset.src;

  entry.target.addEventListener('load', function () {
    entry.target.classList.remove('lazy-img');
  });

  observer.unobserve(entry.target);
};

const imgObserver = new IntersectionObserver(loadImg, {
  root: null,
  threshold: 0,
  rootMargin: '200px',
});

imgTargets.forEach(img => imgObserver.observe(img));

// Slider
const slides = document.querySelectorAll('.slide');
const btnLeft = document.querySelector('.slider__btn--left');
const btnRight = document.querySelector('.slider__btn--right');

let curSlide = 0;
const maxSlide = slides.length;

// const slider = document.querySelector('.slider');
// slider.style.transform = 'scale(0.4)';
// slider.style.overflow = 'visible';

const goToSlide = function (slide) {
  slides.forEach(
    (s, i) => (s.style.transform = `translateX(${100 * (i - slide)}%)`)
  );
};

goToSlide(0);

// Next slide
const nextSlide = function () {
  if (curSlide === maxSlide - 1) {
    curSlide = 0;
  } else {
    curSlide++;
  }

  goToSlide(curSlide);
};

const prevSlide = function () {
  if (curSlide === 0) {
    curSlide = maxSlide - 1;
  } else {
    curSlide--;
  }
  goToSlide(curSlide);
};
btnRight.addEventListener('click', nextSlide);
btnLeft.addEventListener('click', prevSlide);

